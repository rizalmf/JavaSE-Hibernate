    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        Asisten asisten = new Asisten();
        
        try {
            asisten.setNama(tfNama.getText());
            asisten.setNim(tfNim.getText());
            asisten.setAngkatan(Integer.parseInt(tfAngkatan.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Harap masukkan data yang valid");
            reset();
            return;
        }
        
        asistenDao.save(asisten);
        reset();
        showData();
        
        // TODO add your handling code here:
    }//GEN-LAST:event_bSimpanActionPerformed

    private void bUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUpdateActionPerformed
        int tabel = tabelAsisten.getSelectedRow();
        if(tabel == -1){
            JOptionPane.showMessageDialog(null, "Pilih salah satu data dalam tabel");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(null, "Update data?", "konfirmasi", JOptionPane.OK_CANCEL_OPTION);
        if(confirm == JOptionPane.OK_OPTION){
            Asisten asisten = new Asisten();
        
            try {
                asisten.setId(Long.parseLong(tfId.getText()));
                asisten.setNama(tfNama.getText());
                asisten.setNim(tfNim.getText());
                asisten.setAngkatan(Integer.parseInt(tfAngkatan.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Harap masukkan data yang valid");
                reset();
                return;
            }
        
            asistenDao.save(asisten);
            reset();
            showData();
        }
        
        
        // TODO add your handling code here:
    }//GEN-LAST:event_bUpdateActionPerformed

    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        int tabel = tabelAsisten.getSelectedRow();
        if(tabel == -1){
            JOptionPane.showMessageDialog(null, "Pilih salah satu data dalam tabel");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(null, "Hapus data?", "konfirmasi", JOptionPane.OK_CANCEL_OPTION);
        if(confirm == JOptionPane.OK_OPTION){
            Asisten asisten = new Asisten();
            
            try {
                asisten.setId(Long.parseLong(tfId.getText()));
                asisten.setNama(tfNama.getText());
                asisten.setNim(tfNim.getText());
                asisten.setAngkatan(Integer.parseInt(tfAngkatan.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal hapus data. Error: "+e);
                reset();
                return;
            }
            
            asistenDao.delete(asisten);
            reset();
            showData();
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_bHapusActionPerformed

