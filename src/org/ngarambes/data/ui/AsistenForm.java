    private void showData(){
        
        Object[] column = {"ID", "Nama", "NIM", "Angkatan"};
        Object[][] data = new Object[asistenDao.getAsistens().size()][4]; //4 adalah jumlah kolom
        
        int i = 0;
        for(Asisten a: asistenDao.getAsistens()){
            Object[] arr_data = {a.getId(), a.getNama(), a.getNim(), a.getAngkatan()};
            data[i] = arr_data;
            i++;
        }
        
        tabmode = new DefaultTableModel(data, column);
        tabelAsisten.setModel(tabmode);        
    }
    
